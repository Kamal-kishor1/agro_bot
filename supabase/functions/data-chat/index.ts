import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { message } = await req.json();
    console.log("Processing question:", message);
    
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    // System prompt that defines the agent's role and capabilities
    const systemPrompt = `You are an expert data analyst specializing in Indian agriculture and meteorological data from data.gov.in. 

Your capabilities:
- Analyze crop production data across states and districts
- Compare rainfall patterns and climate data
- Provide insights on agriculture-climate correlations
- Support policy recommendations with data-backed arguments
- Always cite data sources from data.gov.in

Available datasets (from data.gov.in):
1. Ministry of Agriculture & Farmers Welfare - Crop production statistics by state/district
2. India Meteorological Department (IMD) - Rainfall and climate data
3. State-wise agricultural statistics
4. District-wise crop yield data

When answering:
1. Parse the user's question to identify required datasets
2. Provide clear, data-backed answers
3. Always cite the specific dataset(s) used
4. For comparisons, present data in a structured format
5. Include relevant statistics and trends
6. Make recommendations based on data patterns

Note: In this demo version, you'll provide realistic sample responses based on typical patterns in Indian agricultural data. Always mention the data source and acknowledge this is a demonstration.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: message },
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI service payment required. Please contact administrator." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("Data chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
